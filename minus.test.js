const sum = require('./minus');
// 2 - 1 เท่ากับ 1
test('2 - 1 เท่ากับ 1', ()=> {
  expect(sum(2,1)).toBe(1);
});
// 12 - 3 เท่ากับ 9
test('12 - 3 เท่ากับ 9', ()=> {
    expect(sum(12,3)).toBe(9);
});
// 20 - 13 เท่ากับ 7
test('20 - 13 เท่ากับ 7', ()=> {
    expect(sum(20,13)).toBe(7);
});
// 2 - 2 เท่ากับ 0
test('2 - 2 เท่ากับ 0', ()=> {
    expect(sum(2,2)).toBe(0);
});
// 2 - 3 เท่ากับ -1
test('22 - 3 เท่ากับ 19', ()=> {
    expect(sum(22,3)).toBe(19);
});